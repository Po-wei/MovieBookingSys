
public class Ticket
{
    //You can change this class to anything you like
    String id;
    String movieName;
    String startTime;
    String seatInfo;
    String hall;

    Ticket(String id, String movieName, String startTime, String seatInfo, String hall){
        this.id = id;
        this.movieName = movieName;
        this.startTime = startTime;
        this.seatInfo = seatInfo;
        this.hall = hall;
    }

}
